	static int multiplyFive( String numbers ) {
		int[] num = new int[5];
		
		for( int i = 0; i < 5; i++ ) {
			num[i] = Integer.parseInt( "" + numbers.charAt(i) );
		}
		
		return num[0]*num[1]*num[2]*num[3]*num[4];
	}
	
	static String getFive( int offset ) {
		return NUMBER.substring( offset, offset + 5 );
	}
	
	public static void main( String[] args ) {
		String fiveDigits;
		int solution = 0;
		int product;
		
		for( int i = 0; i < NUMBER.length() - 5; i++ ) {
			fiveDigits = getFive( i );
			
			product = multiplyFive( fiveDigits );
			
			if( product > solution ) {
				solution = product;
			}
		}
		
		System.out.println( "The max product is: " + solution );
	}
